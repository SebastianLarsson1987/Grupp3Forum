<strong>Bakgrund</strong><br>
EU har antagit en Dataskyddsförordning, kallad GDPR. GDPR har till syfte att öka skyddet för
personuppgifter och den personliga integriteten för alla EU-medborgare. Med GDPR införs ett
omfattande regelverk med både rättigheter för den registrerade och med skyldigheter som ställer krav
på en organisationsstruktur och förmåga att behandla personuppgifter på ett korrekt sätt.
Vi behandlar dina personuppgifter främst för att fullfölja våra förpliktelser mot dig. Vår utgångspunkt
är att inte behandla fler personuppgifter än vad som behövs för ändamål, och vi strävar alltid efter att
använda de minst integritetskänsliga uppgifterna.
<br>
<br>
<strong>Riktlinjer</strong><br>
Vilka personuppgifter behandlar vi?
Vi behandlar endast personuppgifter när vi har laglig grund. Vi behandlar inte personuppgifter i annat
fall än när de behövs för att fullgöra förpliktelser enligt avtal och lag. Här följer exempel på
personuppgifterna vi behandlar:<br><br>
 UserName<br>
 E-postadress<br>
 Lösenord<br>
<br>
<br>
<strong>Hur får vi tillgång till dina personuppgifter?</strong><br>
Vi försöker i möjligaste mån att inhämta ditt samtycke innan vi börjar behandlar dina personuppgifter.
Du samtycker till behandling genom att acceptera våra allmänna villkor. När du samtycker till våra
allmänna villkor samtycker du också till att vi behandlar dina personuppgifter.
Du har när som helst rätt att återkalla ditt samtycke. Vi kommer då inte längre att behandla dina
personuppgifter eller inhämta nya, under förutsättning att det inte behövs för att fullgöra våra
skyldigheter enligt avtal eller lag. Tänk på att återkallelse av samtycke kan innebära att vi inte kan
fullgöra de skyldigheter vi har i förhållande till dig.
